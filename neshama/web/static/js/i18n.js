// Current language - default to Chinese
let currentLang = localStorage.getItem('neshama-lang') || 'zh';

// Get translation by key
function t(key) {
    const lang = LANGUAGES[currentLang] || LANGUAGES.zh;
    return lang[key] || key;
}

// Set language
function setLang(lang) {
    if (LANGUAGES[lang]) {
        currentLang = lang;
        localStorage.setItem('neshama-lang', lang);
        
        // Update all nav items
        updateNavLabels();
        
        // Update connection status text
        updateConnectionStatus();
        
        // Re-render current page to apply new language
        if (typeof router !== 'undefined' && router.pages[AppState.currentPage]) {
            router.pages[AppState.currentPage].render();
        }
        
        // Dispatch custom event for other components
        document.dispatchEvent(new CustomEvent('langChange', { detail: { lang } }));
    }
}

// Toggle language between zh and en
function toggleLang() {
    setLang(currentLang === 'zh' ? 'en' : 'zh');
}

// Update navigation labels
function updateNavLabels() {
    const navItems = document.querySelectorAll('.nav-item');
    navItems.forEach(item => {
        const page = item.dataset.page;
        const textEl = item.querySelector('.nav-text');
        if (textEl) {
            const key = `nav.${page}`;
            textEl.textContent = t(key);
        }
    });
    
    // Update language toggle button
    const langBtn = document.getElementById('lang-toggle-btn');
    if (langBtn) {
        langBtn.textContent = currentLang === 'zh' ? 'EN' : '中';
    }
}

// Update connection status text
function updateConnectionStatus() {
    const statusText = document.querySelector('.status-text');
    if (statusText) {
        statusText.textContent = t('common.connected');
    }
}

// Get OCEAN labels for charts
function getOceanLabels() {
    return [
        t('ocean.openness'),
        t('ocean.conscientiousness'),
        t('ocean.extraversion'),
        t('ocean.agreeableness'),
        t('ocean.neuroticism')
    ];
}

// Get current language
function getCurrentLang() {
    return currentLang;
}

// Initialize i18n on load
document.addEventListener('DOMContentLoaded', () => {
    // Small delay to ensure DOM is ready
    setTimeout(() => {
        updateNavLabels();
        updateConnectionStatus();
    }, 10);
});
