namespace Neshama.SDK.Enums
{
    /// <summary>
    /// 九种基础情绪类型，对应后端的九种基础情绪
    /// </summary>
    public enum EmotionType
    {
        /// <summary>喜悦 - 开心、愉快的情绪</summary>
        Joy,
        
        /// <summary>悲伤 - 难过、沮丧的情绪</summary>
        Sadness,
        
        /// <summary>愤怒 - 生气、不满的情绪</summary>
        Anger,
        
        /// <summary>恐惧 - 害怕、担心的情绪</summary>
        Fear,
        
        /// <summary>惊讶 - 意外、震惊的情绪</summary>
        Surprise,
        
        /// <summary>厌恶 - 反感、讨厌的情绪</summary>
        Disgust,
        
        /// <summary>信任 - 相信、依赖的情绪</summary>
        Trust,
        
        /// <summary>期待 - 希望、盼望的情绪</summary>
        Anticipation,
        
        /// <summary>羞愧 - 尴尬、自责的情绪</summary>
        Shame
    }
}
